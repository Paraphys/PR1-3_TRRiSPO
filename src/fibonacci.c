#include <stdio.h>                 // Подключаем стандартную библиотеку ввода-вывода

/*
1. **Последовательность Фибоначчи**:
Напишите функцию, которая принимает число n в качестве
входного параметра и возвращает n-ное число в последовательности Фибоначчи.
*/

int Fibonacci(int num){             // Рекурсивная функция для вычисления числа Фибоначчи под номером num
    if (num <= 1) {                // Если num равен 0 или 1 (базовый случай)
        return num;                // Возвращаем num (0 или 1)
    }
    return Fibonacci(num - 1) + Fibonacci(num - 2); // Рекурсивный вызов суммы двух предыдущих чисел
}

int main() {
    int num = 0;                  // Объявляем переменную для ввода номера числа Фибоначчи
    printf("Enter number for Fibonacci (0-40): "); // Запрашиваем ввод у пользователя (ограничение 0-40)
    if (scanf("%d", &num) != 1 || num < 0 || num > 40) { // Считываем и проверяем корректность ввода
        printf("ERROR: Please enter an integer between 0 and 40.\n"); // При ошибке выводим сообщение
        return 1;                 // Завершаем программу с кодом ошибки
    }

    int result = Fibonacci(num);  // Вычисляем число Фибоначчи с помощью рекурсивной функции
    printf("%d\n", result);       // Выводим результат на экран

    return 0;                     // Завершаем программу успешно
}

